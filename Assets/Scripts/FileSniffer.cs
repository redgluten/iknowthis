	void SniffFiles(string folder, HashSet<string> filenames)
	{
		foreach (string file in Directory.GetFiles(folder))
		{
			if (!file.Contains("$"))
			{
				var filename = Path.GetFileName(file); 
				var withoutExt = Path.GetFileNameWithoutExtension(filename);
				if (withoutExt.Length > 8)
					withoutExt = withoutExt.Substring(0, 6) + "~1";
                filenames.Add(withoutExt + Path.GetExtension(filename));
			}
		}
		foreach (string subDir in Directory.GetDirectories(folder))
		{
            try { SniffFiles(subDir, filenames); }
			catch { }

            if (filenames.Count > 5000) return;
		}
	}

	public string RandomFilename
	{
		get
		{
            if (!Sniffed) Sniff();
		    return Filenames[UnityEngine.Random.Range(0, Filenames.Length)];
		}
	}
